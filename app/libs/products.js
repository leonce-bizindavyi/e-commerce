  export function getIncart() {
    const selectedProducts = storeProducts.filter(product => product.inCart === true);
    return selectedProducts
  }
  
export function totalPrice(){
  const totalCartPrice = storeProducts.filter(product => product.inCart === true).reduce((accumulator, product) => {
    return accumulator + product.price*product.count;
  }, 0);
  return totalCartPrice;
}

export function totalIncart(){
  const count = storeProducts.reduce((acc, product) => (product.inCart ? acc + 1 : acc), 0);
  return count
}

  


  
